    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         try
               {
                   if(t4.getText().equalsIgnoreCase(""))
                   {
                       JOptionPane.showMessageDialog(null,"Please Fill Answer Field","Error",JOptionPane.ERROR_MESSAGE);
                   }    
                   else
                   {
                   String url="jdbc:oracle:thin:@localhost:1521:XE";
                   Class.forName("oracle.jdbc.driver.OracleDriver");
                   Connection conn=DriverManager.getConnection(url,"Admin","abc12345");
                   PreparedStatement pst=conn.prepareStatement("Select * from Login where USERNAME=? AND ANSWER=?");
                   pst.setString(1,t1.getText());
                   pst.setString(2,t4.getText());
                   ResultSet rs=pst.executeQuery();
                   if(rs.next())
                   {
                       t5.setText(rs.getString(3));
                   }
                   else
                   {
                       t5.setText("");
                       t4.setText("");
                       JOptionPane.showMessageDialog(null,"Wrong Answer","Error",JOptionPane.ERROR_MESSAGE);
                   }
                   conn.close();
                  }
               }
               catch(ClassNotFoundException e)
               {
                   JOptionPane.showMessageDialog(null,"!! Driver Not Loaded, can't connect with database","Database Error",JOptionPane.ERROR_MESSAGE);
               }
               catch(SQLException e)
               {
                   JOptionPane.showMessageDialog(null,e.getMessage(),"Database",JOptionPane.ERROR_MESSAGE);
               }
    }//GEN-LAST:event_jButton3ActionPerformed

