    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          try
               {
                   if(t1.getText().equals(""))
                   {
                       JOptionPane.showMessageDialog(null,"Please Fill User Name Field","User Name Error",JOptionPane.ERROR_MESSAGE);
                   }    
                   else
                   {
                   String url="jdbc:oracle:thin:@localhost:1521:XE";
                   Class.forName("oracle.jdbc.driver.OracleDriver");
                   Connection conn=DriverManager.getConnection(url,"Admin","abc12345");
                   PreparedStatement pst=conn.prepareStatement("Select * from Login where USERNAME=?");
                   pst.setString(1,t1.getText());
                   ResultSet rs=pst.executeQuery();
                   if(rs.next())
                   {
                       t2.setText(rs.getString(1));
                       t3.setSelectedIndex(rs.getInt(4));
                       t3.setEnabled(false);
                   }
                   else
                   {
                       JOptionPane.showMessageDialog(null,"User "+t1.getText()+" Not Found","Error",JOptionPane.ERROR_MESSAGE);
                       t1.setText("");
                       t2.setText("");
                       t3.setSelectedIndex(0);
                   }
                   conn.close();
                  }
               }
               catch(ClassNotFoundException e)
               {
                   JOptionPane.showMessageDialog(null,"!! Driver Not Loaded, can't connect with database","Database Error",JOptionPane.ERROR_MESSAGE);
               }
               catch(SQLException e)
               {
                   JOptionPane.showMessageDialog(null,e.getMessage(),"Database",JOptionPane.ERROR_MESSAGE);
               }
    }//GEN-LAST:event_jButton2ActionPerformed

