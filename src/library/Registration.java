    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
 try
               {
                   String name=tname.getText().trim();
                   String uname=tusername.getText().trim();
                   String pass=tpassword.getText().trim();
                   String cpass=tcpassword.getText().trim();
                   int ques=tquestion.getSelectedIndex();
                   String answer=tanswer.getText().trim();
                   if(name=="" || uname=="" || pass=="" || cpass=="" || ques==0 || answer.equals("")|| !pass.equals(cpass))
                   {
                        if(!pass.equals(cpass))
                          throw new SQLException("Password And Confirm Password Not Matched");
                        else if(ques==0)
                            throw new SQLException("Please Select Security Question !");
                        else
                          throw new SQLException("Please Fill All Fields !");
                   }
                   else
                   {
                   String url="jdbc:oracle:thin:@localhost:1521:XE";
                   Class.forName("oracle.jdbc.driver.OracleDriver");
                   Connection conn=DriverManager.getConnection(url,"Admin","abc12345");
                   PreparedStatement pst=conn.prepareStatement("insert into login values(?,?,?,?,?)");
                   pst.setString(1,name);
                   pst.setString(2,uname);
                   pst.setString(3,pass);
                   pst.setInt(4,ques);
                   pst.setString(5,tanswer.getText()); 
                   pst.executeUpdate();
                   JOptionPane.showMessageDialog(null,"Registration Successful, Now you can login into your Account","Registration Successful",JOptionPane.INFORMATION_MESSAGE);
                   new Login().setVisible(true);
                   conn.close();
                   this.dispose();
                  }
               }
               catch(ClassNotFoundException e)
               {
                   JOptionPane.showMessageDialog(null,"!! Driver Not Loaded, can't connect with database","Database Error",JOptionPane.ERROR_MESSAGE);
               }
               catch(SQLException e)
               {
                   if(e.getErrorCode()==1)
                   {
                       JOptionPane.showMessageDialog(null,tusername.getText()+" is Already Available","Database",JOptionPane.ERROR_MESSAGE);
                       tusername.setText("");
                      
                       
                   }
                   else
                      JOptionPane.showMessageDialog(null,e.getMessage(),"SQL ERROR",JOptionPane.ERROR_MESSAGE);
                    tusername.requestFocus();
               }
    }//GEN-LAST:event_jButton1ActionPerformed

